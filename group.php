	  Everyone in the world have things that they cherish, are at least we use every day. It could be our smart phones, our clothes, or even the old stuffed toys we hug every night when we go to sleep. 
	  <br><br>
	  But as time passes, the things we use almost every day, turn into things that we set aside under our beds or inside our closets. This is because like us, our things also get old. Some become defective, others we don’t use because we lost interest in. Almost inside any home there are old things that are not used anymore and ending up just collecting dust.
	  <br><br>
	  These can be old clothes, books, gadgets, toys or any other things that we used and took interest in. Even if we notice our old stuff, we can’t think of any way we could use them anymore. This leads us to either leaving them to clutter up, donating them for charity and worse is throwing them away. 
We even buy new things that is only the newer version of the old things we set aside at home. There are even reports that people nowadays consume twice as many material goods today as we did 50 years ago. [1] This may also due to the amount of consumer advertisements and goods available in the market than years before.
	  <br><br>
 There are more malls available now, only shopping is the norm and different trends in clothing and technology change at a faster pace. And because of all the clutter in our homes we end up losing our things rummaging through our stuff. It is said that over the course of our lifetime, we will spend a total of 2.5 days a year looking for misplaced items. The most items that are misplaced are TV remotes (45%), phones (33%), car & keys (28%), glasses (27%), shoes (24%) and wallets/purses (20%). [2]
That is why rather than abandoning our things in the corners of your home we find ways of making use of our stuff at home. Some can be given for charity, but other things are valuable to the point of thinking that it is such a waste to give away. This is where other people resort to opening a garage sale or selling them online. But these strategies are not full proof.
<br><br>
Our team thought of creating an application where people can share the old things that they do not use anymore. Where they can share images and videos clips of the stuff, they deem to still be adequate and trade them with other items they may like that other people have no use for anymore. 
It is like a social platform where communities could be created gathering of the things they once used and still take interests. People could simply upload an image; of the thing they want to trade away place a description of the necessary information and communicate with the ones that are interested. They could privately message each other to settle on how they can settle the trades by meeting up on places closest to their location.
Our team are also planning on creating a delivery service of our own, where items can only be delivered if the quality of the goods match the description, they have set online. And unlike years before where using the old things other people used are a sort of taboo, more people settle with secondhand consumption. [3]
<br><br><br>	
	An application like this could help the people to reduce clutter inside their closets, reduce throwing away things that end up as garbage, and even save up money. Having a mindset of using things the things we can still use and trying to ask others if they could make use of the stuff, we can’t could greatly help not only ourselves but to all the people around us. 
Summary:
<br>
Problems:<br>
-There is only limited information given in the user’s profile in the current social media sites available<br>
-Facebook and other social media sites are primarily not a marketplace. The websites purposes are mostly for socializing <br>
-Preloved items are not used and hoarded by the owners <br>
-Users can’t find a place to simply donate or trade their preloved items <br>
-Assurance of the quality of product<br>
-There is no centralized application, where you can find someone to donate to or trade to<br>
Solutions:<br>
-Implementing a delivery service for the user’s items<br>
-Make an application that can be used for donating or trading items with different people<br>
-Donors or traders will be informed if their products were already received<br>
-Centralizing all organizations, whether it be a donor or a receiver into environment<br>
